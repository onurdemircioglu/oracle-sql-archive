SELECT YEAR_MONTH_01
        ,SUBSTR(YEAR_MONTH_01 ,1 ,4) || SUBSTR(YEAR_MONTH_01 ,-2 ,2) -1 AS PREVIOUS_PERIOD_01
        ,SUBSTR(YEAR_MONTH_01 ,1 ,4) || SUBSTR(YEAR_MONTH_01 ,-2 ,2) +1 AS NEXT_PERIOD_01

        ,YEAR_MONTH_02
        ,CASE WHEN SUBSTR(YEAR_MONTH_02 ,-2 ,2) = 1 THEN TO_NUMBER(SUBSTR(YEAR_MONTH_02 ,1 ,4) - 1 || 12)
            ELSE SUBSTR(YEAR_MONTH_02 ,1 ,4) || SUBSTR(YEAR_MONTH_02 ,-2 ,2) -1
        END AS PREVIOUS_PERIOD_02
        ,CASE WHEN SUBSTR(YEAR_MONTH_02 ,-2 ,2) = 12 THEN TO_NUMBER(SUBSTR(YEAR_MONTH_02 ,1 ,4) + 1 || '01')
            ELSE SUBSTR(YEAR_MONTH_02 ,1 ,4) || SUBSTR(YEAR_MONTH_02 ,-2 ,2) +1
        END AS NEXT_PERIOD_02

        ,YEAR_MONTH_03
        ,CASE WHEN SUBSTR(YEAR_MONTH_03 ,-2 ,2) = 1 THEN TO_NUMBER(SUBSTR(YEAR_MONTH_03 ,1 ,4) - 1 || 12)
            ELSE SUBSTR(YEAR_MONTH_03 ,1 ,4) || SUBSTR(YEAR_MONTH_03 ,-2 ,2) -1
        END AS PREVIOUS_PERIOD_03
        ,CASE WHEN SUBSTR(YEAR_MONTH_03 ,-2 ,2) = 12 THEN TO_NUMBER(SUBSTR(YEAR_MONTH_03 ,1 ,4) + 1 || '01')
            ELSE SUBSTR(YEAR_MONTH_03 ,1 ,4) || SUBSTR(YEAR_MONTH_03 ,-2 ,2) +1
        END AS NEXT_PERIOD_03

        ,TO_CHAR(ADD_MONTHS(TO_DATE(YEAR_MONTH_01 || '01', 'YYYYMMDD'),-1), 'YYYYMM') AS PREVIOUS_PERIOD_01_V2
        ,TO_CHAR(ADD_MONTHS(TO_DATE(YEAR_MONTH_01 || '01', 'YYYYMMDD'),1), 'YYYYMM') AS NEXT_PERIOD_01_V2

        ,TO_CHAR(ADD_MONTHS(TO_DATE(YEAR_MONTH_02 || '01', 'YYYYMMDD'),-1), 'YYYYMM') AS PREVIOUS_PERIOD_02_V2
        ,TO_CHAR(ADD_MONTHS(TO_DATE(YEAR_MONTH_02 || '01', 'YYYYMMDD'),1), 'YYYYMM') AS NEXT_PERIOD_02_V2

        ,TO_CHAR(ADD_MONTHS(TO_DATE(YEAR_MONTH_03 || '01', 'YYYYMMDD'),-1), 'YYYYMM') AS PREVIOUS_PERIOD_03_V2
        ,TO_CHAR(ADD_MONTHS(TO_DATE(YEAR_MONTH_03 || '01', 'YYYYMMDD'),1), 'YYYYMM') AS NEXT_PERIOD_03_V2

FROM
    (
    SELECT 202108 AS YEAR_MONTH_01
            ,202101 AS YEAR_MONTH_02
            ,202112 AS YEAR_MONTH_03
    FROM DUAL
    )
;
