SELECT TO_CHAR(SYSDATE ,'DD/MM/YYYY HH24:MI:SS') AS DATE_00
        ,TO_CHAR(SYSDATE + 1,'DD/MM/YYYY HH24:MI:SS') AS NEXT_DAY
        ,TO_CHAR(SYSDATE + 1/24,'DD/MM/YYYY HH24:MI:SS') AS ONE_HOUR_LATER
        ,TO_CHAR(SYSDATE + 1/24/60,'DD/MM/YYYY HH24:MI:SS') AS ONE_MINUTE_LATER -- OR 1/1440 (24*60)
        ,TO_CHAR(SYSDATE + 1/24/60/60,'DD/MM/YYYY HH24:MI:SS') AS ONE_SECOND_LATER -- OR 1/86400 (24*60*60)
        ,TO_CHAR(SYSDATE + 5/24/60/60,'DD/MM/YYYY HH24:MI:SS') AS FIVE_SECOND_LATER
FROM DUAL
;
